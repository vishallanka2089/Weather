//Fetching Current Location when page loads
window.addEventListener("load", ()=> {
    let long;
    let lat;
    if (navigator.geolocation) {
        navigator.geolocation.getCurrentPosition(position => {
            console.log(position);
            long = position.coords.longitude;
            lat = position.coords.latitude;

            const api = `https://api.openweathermap.org/data/2.5/weather?lat=${lat}&lon=${long}&appid=723790ade36d14263fd8423118f5987f`;
            console.log(api);
            fetch(api)
                .then(response => response.json())
                .then(data => {
                    let city = data.name;
                    console.log(city)
                    weather.fetchWeather(`${city}`)
  

                });
            
        });
    }
});

